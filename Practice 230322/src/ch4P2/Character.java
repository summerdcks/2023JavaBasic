package ch4P2;

public class Character {
	String name;
	int age;
	float offencePower;
	float difensePower;
	
	public Character() {
		this();
	}
	
	public Character(String name, int age, float offencePower, float difensePower) {
		this.name = name;
		this.age = age;
		this.offencePower = offencePower;
		this.difensePower = difensePower;
	}
	
	
}
