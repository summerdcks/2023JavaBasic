package daily0317;

public class Variable2 {
	public static void main(String[] arg) {
		char cTemp = 'A';
		System.out.println(cTemp);
		cTemp = 'B';
		System.out.println(cTemp);
		cTemp = 'C';
		System.out.println(cTemp);
		int i = 123;
		System.out.println(i);
		i = 456;
		System.out.println(i);
	}
}